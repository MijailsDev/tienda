CREATE DATABASE TIENDA;
USE TIENDA;
CREATE TABLE CLIENTE
(
  CODIGO_CLIENTE VARCHAR(5) PRIMARY KEY,
  NOMBRES_CLIENTE VARCHAR(20),
  APELLIDOS_CLIENTE VARCHAR(20),
  FECHA_NACIMIENTO DATE,
  TELEFONO_CLIENTE VARCHAR(11)
);
CREATE TABLE PROVEEDOR
(
  CODIGO_PROVEEDOR VARCHAR(5) PRIMARY KEY,
  NOMBRES_PROVEEDOR VARCHAR(20),
  APELLIDOS_PROVEEDOR VARCHAR(20),
  DIRECCION_PROVEEDOR VARCHAR(20)
);
CREATE TABLE PRODUCTO
(
  CODIGO_PRODUCTO VARCHAR(5) PRIMARY KEY,
  DESCRIPCION_PRODUCTO VARCHAR(35),
  CATEGORIA_PRODUCTO VARCHAR(25),
  PRECIO_PRODUCTO DOUBLE,
  CODIGO_PROVEEDOR VARCHAR(5),
  FOREIGN KEY (CODIGO_PROVEEDOR) REFERENCES PROVEEDOR(CODIGO_PROVEEDOR)
);
CREATE TABLE COMPRA
(
  CANTIDAD_COMPRA INT,	
  CODIGO_CLIENTE VARCHAR(5),
  FOREIGN KEY (CODIGO_CLIENTE) REFERENCES CLIENTE(CODIGO_CLIENTE),
  CODIGO_PRODUCTO VARCHAR(5),
  FOREIGN KEY (CODIGO_PRODUCTO) REFERENCES PRODUCTO(CODIGO_PRODUCTO)
);
