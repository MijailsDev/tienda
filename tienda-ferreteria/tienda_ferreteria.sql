CREATE DATABASE TIENDA_FERRETERIA;
USE TIENDA_FERRETERIA;
CREATE TABLE CLIENTE
(
CODIGO_CLIENTE VARCHAR(4) PRIMARY KEY,
NOMBRE_CLIENTE VARCHAR(30),
FNACIMIENTO_CLIENTE DATE
);
CREATE TABLE PRODUCTO
(
CODIGO_PRODUCTO VARCHAR(4) PRIMARY KEY,
NOMBRE_PRODUCTO VARCHAR(40),
DESCRIPCION_PRODUCTO VARCHAR(40),
PRECIO_PRODUCTO DOUBLE
);
CREATE TABLE RECIBO
(
NUMERO_RECIBO VARCHAR(5) PRIMARY KEY,
FECHA_RECIBO DATE,
CODIGO_CLIENTE VARCHAR(4),
FOREIGN KEY (CODIGO_CLIENTE) REFERENCES CLIENTE(CODIGO_CLIENTE)
);
CREATE TABLE LINEA_RECIBO
(
NUMERO_LRECIBO INT PRIMARY KEY,
CANTIDAD_LRECIBO DECIMAL,
CODIGO_PRODUCTO VARCHAR(4),
FOREIGN KEY (CODIGO_PRODUCTO) REFERENCES  PRODUCTO(CODIGO_PRODUCTO),
NUMERO_RECIBO VARCHAR(5),
FOREIGN KEY (NUMERO_RECIBO) REFERENCES RECIBO(NUMERO_RECIBO)
);

