/*REPORTE LAS DESCRIPCIONES DE LOS PRODUCTOS CON SU LAS SUMAS DE LOS PRECIOS POR CADA DESCRIPCION MAYORES QUE 45*/
SELECT DESCRIPCION_PRODUCTO, SUM(PRECIO_PRODUCTO) FROM PRODUCTO GROUP BY DESCRIPCION_PRODUCTO HAVING SUM(PRECIO_PRODUCTO)>45;


/*REPORTE LOS NOMBRE Y FECHA DE NACIMIENTO DEL CLIENTE CON SU FECHA DE RECIBO CUYA FECHA DE NACIMIENTO ES DEL AÑO 2012*/
