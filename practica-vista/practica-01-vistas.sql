CREATE DATABASE PRACTICA_VISTA;
USE PRACTICA_VISTA;

CREATE TABLE CONTINENTE
(
  CÓDIGO_CONTINENTE VARCHAR(5) PRIMARY KEY,
  NOMBRE_CONTINENTE VARCHAR(15)
);

CREATE TABLE HABITAT
(
  CÓDIGO_HABITAT VARCHAR(5) PRIMARY KEY,
  NOMBRE_HABITAT VARCHAR(30), 
  CLIMA_HABITAT VARCHAR(15),
  CÓDIGO_CONTINENTE VARCHAR(5),
  FOREIGN KEY (CÓDIGO_CONTINENTE) REFERENCES CONTINENTE(CÓDIGO_CONTINENTE)
);

CREATE TABLE VISITANTE
(
  CÓDIGO_VISITANTE VARCHAR(5) PRIMARY KEY,
  NOMBRE_VISITANTE VARCHAR(15),
  APELLIDOS_VISITANTE VARCHAR(25),
  DNI_VISITANTE VARCHAR(8),
  TIEMPO_DURACIÓN TIME,
  CANTIDAD_VISITAS INT
);

CREATE TABLE ZONA
(
  CÓDIGO_ZONA VARCHAR(5) PRIMARY KEY,
  NOMBRE_ZONA VARCHAR(15),
  EXTENSION_ZONA DOUBLE,
  CÓDIGO_VISITANTE VARCHAR(5),
  FOREIGN KEY (CÓDIGO_VISITANTE) REFERENCES VISITANTE(CÓDIGO_VISITANTE)
);

CREATE TABLE ESPECIE
(
  CÓDIGO_ESPECIE VARCHAR(5) PRIMARY KEY,
  NOMBRE_CIENTÍFICO VARCHAR(20),
  NOMBRE_COMÚN VARCHAR(20),
  ALIMENTACIÓN_ESPECIE VARCHAR(15),
  CÓDIGO_HABITAT VARCHAR(5),
  FOREIGN KEY (CÓDIGO_HABITAT) REFERENCES HABITAT(CÓDIGO_HABITAT),
  CÓDIGO_ZONA VARCHAR(5),
  FOREIGN KEY (CÓDIGO_ZONA) REFERENCES ZONA(CÓDIGO_ZONA)
);

CREATE TABLE CUIDADOR
(
  CÓDIGO_CUIDADOR VARCHAR(5) PRIMARY KEY,
  NOMBRE_CUIDADOR VARCHAR(15),
  APELLIDOS_CUIDADOR VARCHAR(25),
  DNI_CUIDADOR VARCHAR(8),
  DIRECCIÓN_CUIDADOR VARCHAR(20),
  TELEFONO_CUIDADOR VARCHAR(11)
);

CREATE TABLE CUIDA
(
  FECHA_CUIDADOR DATE,
  HORA_CUIDADOR TIME,
  CÓDIGO_ESPECIE VARCHAR(5),
  FOREIGN KEY (CÓDIGO_ESPECIE) REFERENCES ESPECIE(CÓDIGO_ESPECIE),
  CÓDIGO_CUIDADOR VARCHAR(5),
  FOREIGN KEY (CÓDIGO_CUIDADOR) REFERENCES CUIDADOR(CÓDIGO_CUIDADOR)
);

CREATE TABLE GUÍA
(
  CÓDIGO_GUÍA VARCHAR(5) PRIMARY KEY,
  NOMBRE_GUÍA VARCHAR(15),
  APELLIDOS_GUÍA VARCHAR(25),
  DNI_GUÍA VARCHAR(8),
  DIRECCIÓN_GUÍA VARCHAR(20),
  TELEFONO_GUÍA VARCHAR(11),
  SUELDO_GUIA DOUBLE
);

CREATE TABLE LLEVA
(
  FECHA_VISITANTE DATE,
  HORA_VISITANTE TIME,
  CÓDIGO_GUÍA VARCHAR(5),
