FROM CLIENTE, RECIBO 
WHERE CLIENTE.CODIGO_CLIENTE=RECIBO.CODIGO_CLIENTE AND FNACIMIENTO_CLIENTE >= '2012-01-01' AND FNACIMIENTO_CLIENTE <= '2012-12-31';


