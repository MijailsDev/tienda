WHERE LINEA_RECIBO.CODIGO_PRODUCTO=PRODUCTO.CODIGO_PRODUCTO AND CANTIDAD_LRECIBO>=5;


/*REPORTE LOS NOMBRES, DESCRIPCION, PRECIO Y NUMERO_LRECIBO CUYA DESCRIPCION ES UNIDAD*/
SELECT NOMBRE_PRODUCTO, DESCRIPCION_PRODUCTO, PRECIO_PRODUCTO, NUMERO_LRECIBO FROM PRODUCTO, LINEA_RECIBO 
WHERE LINEA_RECIBO.CODIGO_PRODUCTO=PRODUCTO.CODIGO_PRODUCTO AND DESCRIPCION_PRODUCTO='UNIDAD';


/*REPORTE NUMERO, FECHA DE RECIBO Y CANTIDAD_LRECIBO CUYA CANTIDAD ESTA ENTRE 5 Y 10*/
